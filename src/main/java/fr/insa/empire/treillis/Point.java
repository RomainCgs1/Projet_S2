package fr.insa.empire.treillis;

public class Point {

    private double px;
    private double py;

    public Point() {
    }
    
    public double getPx()
    {
        return this.px;
    }
    
    public double getPy()
    {
        return this.py;
    }
    
    private void setPx(double px)
    {
        this.px = px;
    }

    private void setPy(double py)
    {
        this.py = py;
    }
}