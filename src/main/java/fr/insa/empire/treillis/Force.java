package fr.insa.empire.treillis;

public class Force {

    private double fx;
    private double fy;

    public Force() 
    {
        
    }

    public double getFx()
    {
        return this.fx;
    }
    
    public double getFy()
    {
        return this.fx;
    }
    
    private void setFx(double fx)
    {
        this.fx = fx;
    }
    
    private void setFy(double fy)
    {
        this.fy = fy;
    }

}